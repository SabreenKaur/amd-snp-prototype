func NewOptions(root map[string][]*trust.AMDRootCerts) verify.Options {
	return verify.Options{
		CheckRevocations: true,
		Getter:           CachingHTTPSGetter(),
		TrustedRoots:     root,
	}
}

func GetSevGuest(tcs []testing.TestCase, opts *testing.DeviceOptions) (client.Device, map[string][]*trust.AMDRootCerts, map[string][]*trust.AMDRootCerts, trust.HTTPSGetter) {
	if client.UseDefaultSevGuest() {
		sevTestDevice, err := testing.TcDevice(tcs, opts)
		if err != nil {
			fmt.Printf("failed to create test device: %v", err)
		}
		goodSnpRoot := map[string][]*trust.AMDRootCerts{
			"Milan": {
				{
					Product: "Milan",
					ProductCerts: &trust.ProductCerts{
						Ask: sevTestDevice.Signer.Ask,
						Ark: sevTestDevice.Signer.Ark,
					},
				},
			},
		}
		badSnpRoot := map[string][]*trust.AMDRootCerts{
			"Milan": {
				{
					Product: "Milan",
					ProductCerts: &trust.ProductCerts{
						// No ASK, oops.
						Ask: sevTestDevice.Signer.Ark,
						Ark: sevTestDevice.Signer.Ark,
					},
				},
			},
		}
		fakekds, err := testing.FakeKDSFromSigner(sevTestDevice.Signer)
		if err != nil {
			fmt.Printf("failed to create fake KDS from signer: %v", err)
		}
		return sevTestDevice, goodSnpRoot, badSnpRoot, fakekds
	}
	return nil, nil, nil, nil
}
func main() {

	oneRaw := testing.TestRawReport(userZeros1)
	testCase := []testing.TestCase{
		{
			Name:        "zeros and a 1",
			Input:       userZeros1,
			Output:      oneRaw,
			OutputProto: oneReport,
		},
	}
	d, goodRoots, badRoots, kds := GetSevGuest(testCase, &testing.DeviceOptions{Now: time.Now()})
	defer d.Close()

	options := &verify.Options{TrustedRoots: goodRoots, Getter: kds}
	badOptions := &verify.Options{TrustedRoots: badRoots, Getter: kds}

	re, err := client.GetReport(d, userZeros1)
	if err != nil {
		fmt.Println("Error getting report from device: ", err)
		return
	}

	attest := sevsnp.Attestation{Report: re}

	if err := verify.SnpAttestation(&attest, options); err != nil {
		fmt.Printf("SnpAttestation(%v) errored unexpectedly: %v", attest, err)
	}
	wantBad := "error verifying VCEK certificate"
	if err := verify.SnpAttestation(&attest, badOptions); !testing.Match(err, wantBad) {
		fmt.Printf("SnpAttestation(_) bad root test errored unexpectedly: %v, want %s", err, wantBad)
	}

}
